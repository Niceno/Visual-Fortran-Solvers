!==============================================================================!
  subroutine Dense_Mod_Allocate(a, n)
!------------------------------------------------------------------------------!
!   Deallocates memory occupied by Dense_Type                                  !
!------------------------------------------------------------------------------!
  implicit none
!---------------------------------[Arguments]----------------------------------!
  type(Dense_Type) :: a
  integer          :: n
!------------------------------------------------------------------------------!

  a % n = n
  allocate(a % val(n,n))    ! matrix entries
  a % val(:,:) = 0.0

  end subroutine
