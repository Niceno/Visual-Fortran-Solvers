!==============================================================================!
  subroutine Dense_Mod_Deallocate(a)
!------------------------------------------------------------------------------!
!   Deallocates memory occupied by Dense_Type                                  !
!------------------------------------------------------------------------------!
  implicit none
!---------------------------------[Arguments]----------------------------------!
  type(Dense_Type) :: a
!------------------------------------------------------------------------------!

  a % n = 0
  deallocate(a % val)    ! matrix entries

  end subroutine
