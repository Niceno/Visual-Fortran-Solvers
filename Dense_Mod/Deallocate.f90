!==============================================================================!
  subroutine Dense_Deallocate(A)
!------------------------------------------------------------------------------!
!   Deallocates memory occupied by Dense_Type                                  !
!------------------------------------------------------------------------------!
  implicit none
!---------------------------------[Arguments]----------------------------------!
  class(Dense_Type) :: A  !! parent class
!------------------------------------------------------------------------------!

  A % n = 0            ! (re)set dimension to zero
  deallocate(A % val)  ! free matrix entries

  end subroutine
