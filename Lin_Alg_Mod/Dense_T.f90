!==============================================================================!
  subroutine Lin_Alg_Mod_Dense_T(b, a)
!------------------------------------------------------------------------------!
!   Transposes a square (full) matrix.                                         !
!------------------------------------------------------------------------------!
  implicit none
!---------------------------------[Arguments]----------------------------------!
  type(Dense_Type) :: b
  type(Dense_Type) :: a
!-----------------------------------[Locals]-----------------------------------!
  integer :: n, row, col
!==============================================================================!

  n = a % n  ! some checks would be possible

  do row = 1, n
    do col = 1, n
      b % val(col, row) = a % val(row, col)
    end do
  end do

  end subroutine
