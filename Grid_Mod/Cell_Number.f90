!==============================================================================!
  integer function Cell_Number(Grid, i, j, k)
!------------------------------------------------------------------------------!
!>  Returns the cell number based on i, j and k indices in Cartesian grid
!------------------------------------------------------------------------------!
  implicit none
!---------------------------------[Arguments]----------------------------------!
  class(Grid_Type)    :: Grid
  integer, intent(in) :: i, j, k
!-----------------------------------[Locals]-----------------------------------!
  integer :: ni, nj, nk
!==============================================================================!

  ni = Grid % nx
  nj = Grid % ny
  nk = Grid % nz

  Cell_Number = (k-1)*ni*nj + (j-1)*ni + i

  end function

